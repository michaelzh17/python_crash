places = ['Tibet', 'Cuba', 'San Fransisco', 'Hawaii', 'Paris', 'Norway']

#print out original list
print(places)

#print out sorted list using sorted()
print(sorted(places))

#print out original list
print(places)

#print out sorted list in reverse alphabetical
print(sorted(places, reverse=True))

#print out original list
print(places)

#print out list with reversed
places.reverse()
